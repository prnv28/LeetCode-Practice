class Solution {
  public:
    string armstrongNumber(int n) {
        int x = n;
        int num = 0;
        while(x>0){
            int rem = x%10;
            num +=  rem * rem * rem;
            x /=10;
        }
        return num==n?"true":"false";
    }
};

