class Solution
{
    public:
    //Function to find the first position with different bits.
    int posOfRightMostDiffBit(int m, int n)
    {
        int a = m^n;
        if(a==0) return -1;
        int count=0;
        while(a!=0){
            if(a&1) return count+1;
            else a=a>>1;
            count++;
        }
        return count+1;
    }
};

